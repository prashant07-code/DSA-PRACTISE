public class stringMethod {
    public static void main(String[] args) {
        String name = "Prashant";
        System.out.println(name.length()); // length of the string
        System.out.println(name.toUpperCase());// string to upper case
        System.out.println(name.toLowerCase());// string to lower case
        System.out.println(name.charAt(3));// character at index 3
        System.out.println(name.indexOf('a'));// index of a character
        System.out.println(name.lastIndexOf('a'));// last index of a character
        System.out.println(name.substring(2, 5));
    }
}
  