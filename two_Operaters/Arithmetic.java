public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println("sum : " + (a + b));
        System.out.println("product : " + (a * b));
        System.out.println("sub : " + (a-b));
        System.out.println("div : " + (a / b));
        System.out.println("mod : " + (a % b));
        

    }
    
}
