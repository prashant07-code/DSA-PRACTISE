public class casting {
    public static void main(String[] args) {
        float a = 60.45f;
        int b = (int) a;
        double d = 344.4665464;
    
        System.out.println((int) d);
        System.out.println(b);

        char ch = 'a'; // ansi 
        int number = (int) ch;
        System.out.println(number);
        
    }
    
}
