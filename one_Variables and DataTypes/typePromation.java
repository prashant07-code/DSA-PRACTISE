public class typePromation {
    public static void main(String[] args) {
        char a = 'a';
        char b = 'b';
        System.out.println((int)(a));
        System.out.println((int)(b));
        System.out.println(b-a);


        short d = 5;
        byte e = 25;
        char ch = 'c';
        byte bt = (byte) (d+e+ch);
        System.out.println(bt);


        int f = 10;
        float h = 20.25f;
        long c = 25;
        double v = 74;
        double ans = f + h + c + v;
        System.out.println(ans);
    }
    
}
